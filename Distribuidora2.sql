CREATE TABLE Funcionario (

CPF VARCHAR(11),
Nome VARCHAR(50) NOT NULL,
Sexo VARCHAR(10) NOT NULL,
DataNasce DATE NOT NULL,
Email VARCHAR(50) ,
Rua VARCHAR(50) NOT NULL,
Bairro VARCHAR(50) NOT NULL,
Numero VARCHAR(10) NOT NULL,

CONSTRAINT FUNCIONARIO_PK PRIMARY KEY (CPF),
CONSTRAINT FUNCIONARIO_CHECK CHECK(Sexo = 'M' OR Sexo = 'F' OR Sexo = 'Masculino' OR Sexo = 'Feminino')
);

CREATE TABLE Telefone (

CPF VARCHAR(11) ,
Telefone VARCHAR(13),
CONSTRAINT Telefone_PK PRIMARY KEY (CPF,Telefone),
CONSTRAINT Telefone_FK FOREIGN KEY (CPF) REFERENCES Funcionario(CPF)

);

CREATE TABLE Chefe (

CPF VARCHAR(11),

CONSTRAINT Chefe_PK PRIMARY KEY (CPF),
CONSTRAINT Chefe_FK FOREIGN KEY (CPF) REFERENCES Funcionario(CPF)
);

CREATE TABLE Cliente (

CodCliente VARCHAR(10)NOT NULL,
Preco NUMBER(10,2)NOT NULL,
DataCiente DATE NOT NULL,
CPF VARCHAR(11) UNIQUE NOT NULL,

CONSTRAINT Client_PK PRIMARY KEY (CodCliente),
CONSTRAINT Cliente_FK FOREIGN KEY (CPF) REFERENCES Funcionario(CPF)
);

CREATE TABLE Projeto(

CPF VARCHAR(11),
NumID VARCHAR(10),
CodSetor VARCHAR(10),
DataInicio DATE,
Tema VARCHAR(75),

CONSTRAINT Projeto_PK PRIMARY KEY (CPF, NumID),
CONSTRAINT Projeto_FK FOREIGN KEY (CPF) REFERENCES Funcionario(CPF)
);

CREATE TABLE Leiturista(

CPF VARCHAR(11),
Lider VARCHAR(11),

CONSTRAINT Leiturista_PK PRIMARY KEY (CPF),
CONSTRAINT Leiturista_FK1 FOREIGN KEY (CPF) REFERENCES Funcionario(CPF),
CONSTRAINT Leiturista_FK2 FOREIGN KEY (Lider) REFERENCES Leiturista(CPF)
);

CREATE TABLE Equipamento (

CodigoEquipamento VARCHAR(10),
Estado VARCHAR(5) NOT NULL,
DataCompra DATE NOT NULL,

CONSTRAINT Equipamento_PK PRIMARY KEY (CodigoEquipamento),
CONSTRAINT Equipamento_Check CHECK (Estado = 'RUIM' or Estado = 'BOM')
);

CREATE TABLE Servico (

CodServico VARCHAR(10),
Nome VARCHAR(50),
Lider VARCHAR(11),

CONSTRAINT SERVICO_PK PRIMARY KEY(CodServico),
CONSTRAINT SERVICO_FK FOREIGN KEY(Lider) REFERENCES LEITURISTA(CPF)
);

CREATE TABLE Realiza (

CPF VARCHAR(11),
CodServico VARCHAR(10),
CodigoEquipamento VARCHAR(10),
DataRealiza DATE DEFAULT SYSDATE NOT NULL,
Codigo VARCHAR(10),

CONSTRAINT Realiza_PK PRIMARY KEY (CPF ,CodigoEquipamento, CodServico),
CONSTRAINT Realiza_FK1 FOREIGN KEY (CPF) REFERENCES Leiturista(CPF),
CONSTRAINT Realiza_FK2 FOREIGN KEY (CodigoEquipamento) REFERENCES Equipamento(CodigoEquipamento),
CONSTRAINT Realiza_FK3 FOREIGN KEY (CodServico) REFERENCES Servico(CodServico)

);

CREATE TABLE Departamento (

CodigoRelacionamento VARCHAR(10),
Nome VARCHAR(50),
NumProjeto VARCHAR(10),

CONSTRAINT Departamento_PK PRIMARY KEY (CodigoRelacionamento)

);

CREATE TABLE Trabalha (

CPF VARCHAR(11) UNIQUE,
CodigoRelacionamento VARCHAR(10)UNIQUE,
DataTrabalha DATE UNIQUE,
CodOperacao VARCHAR(10)UNIQUE,

CONSTRAINT TrabalhaA_PK PRIMARY KEY (CPF,CodigoRelacionamento,DataTrabalha,CodOperacao),
CONSTRAINT TRABALHA_FK FOREIGN KEY(CPF) REFERENCES FUNCIONARIO(CPF),
CONSTRAINT TRABALHA_FK2 FOREIGN KEY(CodigoRelacionamento) REFERENCES Departamento(CodigoRelacionamento)
);

CREATE TABLE Salario (

CPF VARCHAR(11) UNIQUE NOT NULL,
CodigoRelacionamento VARCHAR(10) UNIQUE NOT NULL,
DataTrabalha DATE UNIQUE NOT NULL,
CodOperacao VARCHAR(10)UNIQUE NOT NULL,
Identificacao VARCHAR(11),
DataSalario DATE,
ValorBruto NUMBER(10,2),
ValorImposto NUMBER(10,2),

CONSTRAINT Salario_PK PRIMARY KEY (Identificacao),
CONSTRAINT Salario_FK FOREIGN KEY (CPF) REFERENCES Trabalha(CPF),
CONSTRAINT Salario_FK2 FOREIGN KEY (CodigoRelacionamento) REFERENCES Trabalha(CodigoRelacionamento),
CONSTRAINT Salario_FK3 FOREIGN KEY (DataTrabalha) REFERENCES Trabalha(DataTrabalha),
CONSTRAINT Salario_FK4 FOREIGN KEY (CodOperacao) REFERENCES Trabalha(CodOperacao)


);

